        <h4>聯賽冠軍或分組賽冠軍交易</h4>
        <div class="type">
          <ul>
            <li>交易指定聯賽最終的冠軍球隊、或分組賽最終的冠軍球隊。</li>
            <li>冠軍在指定的冠軍盤中,所選擇的隊伍或選手獲得最終勝利為符合派彩條件(依標題而定)。在特定的冠軍項目中,超出所預算獲得勝利者的數目(例:最多進球球員/雙冠軍),本公司將依據以下的方程式進行派彩: (注額 / 勝利者數目 x 賠率)-(本金 / 勝利者數目)=實際獲利數目 例:交易最多進球球員,注額1000 賠率4.2,獲得勝利者為2位選手(1000 / 2 x 4.2)- (1000/.2) = 1600 (實際獲利)</li>
            <li>冠軍項目月份榜首與榜尾結算規則：<br>此項目計算方式於該月第一日至最後一日(英國時間23:59)止，如遇最後一日賽事延賽或賽事取消則不予計算，不影響該項目的結算。</li>
          </ul>
