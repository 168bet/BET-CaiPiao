	    <h4>滾球交易</h4>
		<p class="p4">在指定比賽中，比賽開始後的所有交易行為將稱為滾球交易。滾球交易將分別為兩種：<strong>滾球讓球</strong> 及 <strong>滾球大小</strong>。滾球交易時間是以比賽開始後，開放滾球交易至<tt>鑫金沙</tt>終止交易時間為標準。</p>
        <div class="type">
		  <h5>計時器</h5>
          <p class="p5">滾球比賽時間顯示只供會員參考之用,如比賽時間有所誤差,我們概不對此負責。</p>
