	    <h4>單式交易</h4>
	    <div class="type">
		  <h5>獨贏</h5>
		  <p class="p5">在指定球賽中，兩球隊以<strong>平手</strong>的方式(不計算讓<strong>局(Set) </strong>在內)選擇其中一球隊勝出，賽果均以開賽後至最終完場的比數為準。</p>
	    </div><!-- type end -->
	    <div class="type">
		  <h5>讓局(Set)</h5>
		  <p class="p5">在指定球賽中，其中一球隊提供讓<strong>局(Set) </strong>作為比賽條件，賽果均以<strong>開賽之後至最終完場的比數</strong>為準。	      </p>
	    </div><!-- type end -->
	    <div class="type">
		  <h5>大/小 (全場比賽進行之總分<em>(Point)</em>數)</h5>
		  <ul>
	        <li>大/小(全場比賽進行之總<strong>分(Point) </strong>數) ：在指定球賽中當交易該場比賽的大/小(全場比賽進行之總<strong>分(Point) </strong>數)時，若比賽進行之總<strong>分(Point)</strong>數高於該場比賽進行的大/小<strong>分(Point) </strong>數則為大，若比賽進行之總<strong>分(Point)</strong>數低於該場比賽的大/小<strong>分(Point) </strong>數則為小。</li>
	        <li>如賽事結果之比賽進行之總<strong>分(Point) </strong>數恰好等於「大/小<strong>分(Point) </strong>數」，則該交易金額將獲全數退回，賽果均以<strong>開賽之後至最終完場的比數</strong>為準。</li>
		  </ul>
        </div><!-- type end -->
      </div><!-- play end -->

