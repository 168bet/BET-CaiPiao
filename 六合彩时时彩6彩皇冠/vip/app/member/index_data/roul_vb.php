    <div id="intro">
      <h2><span>排球交易規則</span></h2>
      <ul>
        <li>所有排球交易皆以<strong>比賽開打後最終完場的比數</strong>為最終結果，若其後由任何體育紀律委員會重新裁決，而更改之賽果均不予以計算。</li>
        <li>如因氣候、場地、球隊因故退賽等客觀因素，導致賽事中止，並延後再進行。若此賽事未在原定之比賽時間當天結束，則該場比賽之交易視為無效。</li>
        <li>交易所有種類(除過關外) 之排球球賽在不足法定時間內被終止、因氣候、場地等客觀因素使比賽開打時間較原定比賽時間延遲超過十二小時、或被取消，則該場球賽的交易均視為無效，交易金額可獲全數退回。 </li>
        <li>交易過關的任何一場排球球賽，如在不足法定時間內被終止、或較原定比賽時間延遲超過十二小時或被取消，則該場球賽的交易均視為有效，並當作已過關論，惟獲利率概以「1」計算。 </li>
        <li>如球賽提前舉行，比賽開打後已接受之交易將視為無效。在球賽開始前所有已接受之交易均為有效。</li>
        <li>排球賽果計算方式：比賽<strong>五局(Set)</strong>中先贏<strong>三局(Set)</strong>之球隊，為勝方</li>
      </ul>
    </div>
  
