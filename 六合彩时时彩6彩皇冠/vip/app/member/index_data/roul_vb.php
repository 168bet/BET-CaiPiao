      <div class="play"><a name="play2"></a>
	    <h4>滾球交易</h4>
	    <div class="type">
		  <h5>滾球讓局</h5>
		  <ul>
	        <li>在指定比賽中，其中一球隊提供讓<strong>局(Set) </strong>條件，賽果以<strong>開賽之後至最終完場之比賽</strong>結果為準。</li>
	        <li>勝出： 滾球讓<strong>局(Set) </strong>是以 (比賽結果) (交易當時的比賽<strong>局(Set) </strong>數)與交易當時的獲利率比對，作為勝出條件。 </li>
	        <li>交易結果： 滾球讓<strong>局(Set) </strong>交易結果是以符合勝出條件的獲利率作為計算交易結果的基準。</li>
		  </ul>
        </div><!-- type end -->
	    <div class="type">
		  <h5>滾球大小</h5>
		  <ul>
	        <li>在指定比賽中當交易該場比賽的大小(全場比賽進行之總<strong>分(Point)</strong>數)時，若比賽進行之總<strong>分(Point)</strong>數高於該場比賽的大小<strong>分(Point)</strong>數則為大，若比賽進行之總<strong>分(Point)</strong>數低於該場比賽的大小<strong>分(Point)</strong>數則為小。</li>
	        <li>勝出：滾球大小是以(比賽進行之總<strong>分(Point)</strong>數)與交易當時大/小的獲利率比對，作為勝出條件。</li>
	        <li>交易結果：滾球大小交易結果是以符合勝出條件的獲利率作為計算交易結果的基準。</li>
		  </ul>
        </div><!-- type end -->
      </div><!-- play end -->
      <div class="top"><a href="#top"><span>回最上層</span></a></div>
      <div class="play"><a name="play3"></a>
	    <h4>總分數交易<em>(比賽總分(Point)數)</em></h4>
	    <div class="type">
		  <p class="p4">單 / 雙：在指定比賽中，兩球隊在 <strong>比賽開打後最終完場</strong>的法定時間內全場比賽進行總<strong>分(Point)</strong>數的總數為「 單 」或 「 雙 」，賽果均以<strong>開賽之後至最終完場的比數</strong>為準。</p>
		  <ul>
		    <li>例：比賽進行總<strong>分(Point)</strong>數為109：即全場總入球數為<strong>單數</strong></li>
		    <li>例：比賽進行總<strong>分(Point)</strong>數為112：即全場總入球數為<strong>雙數</strong></li>
	      </ul>
	    </div><!-- type end -->
      </div><!-- play end -->
      <div class="top"><a href="#top"><span>回最上層</span></a></div>

      <div class="play"><a name="play4"></a>
	    <h4>波膽交易<em>(比賽局(Set)數)</em></h4>
		<p class="p4">在指定比賽中，選定兩球隊勝負<strong>局(Set) </strong>數的交易，賽果以<strong>比賽開打後最終完場</strong>之比賽結果為準。</p>
      </div><!-- play end -->

      <div class="play"><a name="play5"></a>
	    <h4>標準過關交易</h4>
	    <div class="type">
		  <ul>
	        <li>標準過關是指在一場比賽中，兩球隊以<strong>平手</strong>情況下比賽，賽果以<strong>比賽開打後最終完場</strong>之比賽結果為準。</li>
	        <li>在「標準過關」開放的每一場賽事中，客戶可交易其中一球隊勝出。</li>
	        <li>所有在「標準過關」中公佈之獲利率都可作過關交易（包括二關、三關、四關等）。</li>
		  </ul>
        </div><!-- type end -->
      </div><!-- play end -->
      <div class="top"><a href="#top"><span>回最上層</span></a></div>

      <div class="play"><a name="play6"></a>
	    <h4>讓局/大小過關交易</h4>
	    <div class="type">
		  <ul>
	        <li>讓<strong>局(Set)</strong>過關是指在一場比賽中，其中一球隊提供讓<strong>局(Set)</strong>作為讓賽條件，賽果<strong>比賽開打後最終完場</strong>之比賽結果為準。 </li>
	        <li>大/小(全場比賽進行之總<strong>分(Point)</strong>數) ：在指定球賽中當交易該場比賽的大/小(全場比賽進行之總<strong>分(Point)</strong>數)時，若比賽進行之總<strong>分(Point)</strong>數高於該場比賽進行的大/小<strong>分(Point)</strong>數則為大，若比賽進行之總<strong>分(Point)</strong>數低於該場比賽的大/小<strong>分(Point)</strong>數則為小。賽果以<strong>比賽開打後最終完場</strong>之比賽結果為準。</li>
	        <li>在「讓<strong>局(Set)</strong>過關」開放的每一場賽事中，客戶可交易其中一球隊勝出。 </li>
	        <li>所有在「讓<strong>局(Set)</strong>過關」中公佈之讓<strong>局(Set)</strong>條件及獲利率都可作過關交易（包括三關、四關等）。</li>
		  </ul>
