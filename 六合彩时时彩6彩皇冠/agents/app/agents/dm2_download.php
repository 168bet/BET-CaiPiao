        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 1 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style1"><strong>Q ：想請問為何下載了 2.7 版 DM2 之後，點選恐龍之後卻出現無法顯示網頁呢，連輸入帳號及密碼的地方都沒有？ </strong></p>
          <span class="text01"><strong>A ： </strong>可能網路速度過慢或該網址於當地 ISP 的 DNS 無法解析。</span> </td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 2 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style1"><strong>Q ：為何點選笑臉裡的網站卻出現無法顯示網頁呢？ </strong></p>
          <span class="text01"><strong>A ： </strong>若有此情況發生，通常為當地有屏蔽 IP 的可能，因此原本使用的網址已作了更新，這時可以直接點選笑臉右邊的更新，或是單獨點選網站，按下右鍵，選取更新，再重新連結一次，即可連上網頁，若是第二次再連不上，即重覆點選更新再重新連結，一直到找到更新的網址即可正常運作。 </span></td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 3 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style7"><span class="style1"><strong>Q ：為何下載後無法殺掉自保軟件 </strong></span></p>
          <span class="text01"><strong>A ： </strong>清除自保軟件需要微軟提供的程式，如果該電腦內缺此檔案， DM2 程式會自行下載 。</span> </td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 4 </strong> </span></td>
      </tr>
      <tr>
        <td> <p class="style7"><span class="style1"><strong>Q ：下載軟件時無論是按保存或開啟皆是無法成功下載 ? </strong></span></p>
          <span class="text01"><strong>A ： </strong>有此狀況時要詢問客戶下載時是否是使用影音傳送帶或是 flashget ，如果是的話請他要按取消，不要按確認，這樣就可以成功下載了。 </span></td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 5 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style1"><strong>Q ：如何取消影音傳送帶或是 flashget? </strong></p>
          <span class="text01"><strong>A ： </strong>參閱<a href="#5">續傳軟件設定</a> 。 </span></td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 6 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style7"><span class="style1"><strong>Q ： dm2 只有 2.7 版才能自動更新嗎 ? 以前的版本是否可自動更新 ? </strong></span></p>
          <span class="text01"><strong>A ： </strong>DM2 皆有提供自動更新功能，但是更新時機，會視整體考量，如需最新版本，可自行下載。 </span></td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 7 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style7"><span class="style1"><strong>Q ：只要登出 dm2 ，會自動關閉己開啟的網站嗎 ? </strong></span></p>
          <span class="text01"><strong>A ： </strong>登出 DM2 不會關閉開啟之網站， DM2 關閉時候會關閉公司相關網頁。</span> </td>
      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 8 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style7"><span class="style1"><strong>Q ：會自動清除上網記錄嗎 ? 多久清一次 ? </strong></span></p>
          <span class="text01"><strong>A ： </strong>可以，每 30 秒內清除上網記錄。 </span></td>
      </tr>
      <tr>
