      </tr>
      <tr>
        <td bgcolor="#CCCCCC"> <span class="style7"><strong>問題 10 </strong> </span></td>
      </tr>
      <tr>
        <td><p class="style7"><span class="style1"><strong>Q ：使用 dm2 會影響網速嗎 ?? </strong></span></p>
          <span class="text01"><strong>A ： </strong>不會， dm2 除第一次使用時因為必須清除上網紀錄及相關暫存檔所以會比較慢，但此後使用則一切正常。 </span></td>
